function []=clustering(X,k1,k2,K,C)   % 样本，聚类后的标号，聚类前标号，簇类数=3

%% 任务1~3，计算Jaccard指数，和距离
[p1,p2]=Jaccard(X,k1,k2);   % p1是曼哈顿距离，p2是欧拉距离

%% ----------------------------------- 以下均使用曼哈顿距离p1计算 --------------------------------
%% 任务4-1
[avg,diam,dmin,dcen]=d(X,K,C,k1,p1);

%% 任务4-2，计算DB指数，使用曼哈顿距离
DBI(avg,dcen,K);

%% 任务5
VDM(X,K,p1);